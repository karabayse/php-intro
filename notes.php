<h1>Hypertext Preprocessor Intro</h1>

<h4>PHP is a widely-used, open source scripting language
PHP scripts are executed on the server
PHP is free to download and use</h4>

<h4>It is powerful enough to be at the core of the biggest blogging system on the web (WordPress)!
It is deep enough to run the largest social network (Facebook)!
It is also easy enough to be a beginner's first server side language</h4>

<h4>PHP files can contain text, HTML, CSS, JavaScript, and PHP code
PHP code are executed on the server, and the result is returned to the browser as plain HTML
PHP files have extension ".php"</h4>

<h4>PHP can generate dynamic page content
PHP can create, open, read, write, delete, and close files on the server
PHP can collect form data
PHP can send and receive cookies
PHP can add, delete, modify data in your database
PHP can be used to control user-access
PHP can encrypt data
With PHP you are not limited to output HTML. You can output images, PDF files, and even Flash movies. You can also output any text, such as XHTML and XML.</h4>

<h4>PHP runs on various platforms (Windows, Linux, Unix, Mac OS X, etc.)
PHP is compatible with almost all servers used today (Apache, IIS, etc.)
PHP supports a wide range of databases
PHP is free. Download it from the official PHP resource: www.php.net
PHP is easy to learn and runs efficiently on the server side</h4>

<h4>To start using PHP, you can:
Find a web host with PHP and MySQL support
Install a web server on your own PC, and then install PHP and MySQL</h4>

<h4>If your server has activated support for PHP you do not need to do anything.
Just create some .php files, place them in your web directory, and the server will automatically parse them for you.
You do not need to compile anything or install any extra tools.
Because PHP is free, most web hosts offer PHP support.</h4>

<h4>However, if your server does not support PHP, you must:
install a web server
install PHP
install a database, such as MySQL
The official PHP website (PHP.net) has installation instructions for PHP: http://php.net/manual/en/install.php</h4>

<h4>A PHP script is executed on the server, and the plain HTML result is sent back to the browser.</h4>

<h4>A PHP script can be placed anywhere in the document.
A PHP script starts with '<?php and ends with ?>:'</h4>

<h4>The default file extension for PHP files is ".php".
A PHP file normally contains HTML tags, and some PHP scripting code.</h4>
